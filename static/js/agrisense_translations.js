// Global translation function
function getAgriSenseTranslation(text) {
    if (!text) return text;
    const trimmed = text.trim();
    
    // Direct match
    if (AGRISENSE_DICTIONARY[trimmed]) return AGRISENSE_DICTIONARY[trimmed];
    
    // Case-insensitive match
    for (const [en, ur] of Object.entries(AGRISENSE_DICTIONARY)) {
        if (en.toLowerCase() === trimmed.toLowerCase()) return ur;
    }
    
    // Handle specific cases like "Confidence: 85%"
    if (trimmed.startsWith('Confidence:')) {
        const percent = trimmed.replace('Confidence:', '').trim();
        return 'اعتماد: ' + percent;
    }
    
    // Handle time like "5m ago"
    if (trimmed.match(/^\d+[smhd] ago$/)) {
        const value = trimmed.match(/^\d+/)[0];
        const unit = trimmed.match(/[smhd] ago$/)[0];
        return value + ' ' + AGRISENSE_DICTIONARY[unit];
    }

    return null; // Return null if not in dictionary
}
